CREATE PROCEDURE VIEWBALANCE
@ACCNO INT
AS
	BEGIN
	IF EXISTS(SELECT ACCNO FROM CUSTOMER_REGISTRATION WHERE ACCNO = @ACCNO)
	SELECT ACCNO,NAME,MOB_NO,AGE,BALANCE FROM CUSTOMER_REGISTRATION WHERE ACCNO = @ACCNO;
	END
GO

