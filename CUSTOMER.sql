CREATE PROCEDURE INSERT_CUSTOMER
@NAME VARCHAR(50),
@MOB_NO CHAR(10),
@AGE INT,
@EMAIL VARCHAR(50),
@BALANCE FLOAT,
@AADHAR INT
AS
	BEGIN
	INSERT INTO CUSTOMER_REGISTRATION(NAME,MOB_NO,AGE,EMAIL,BALANCE,AADHAR)
	VALUES(@NAME,@MOB_NO,@AGE,@EMAIL,@BALANCE,@AADHAR)
	END
GO

